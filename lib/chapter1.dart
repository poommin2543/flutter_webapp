        actions: [
          TextButton(
            onPressed: () {
              Navigator.pop(context);
              Navigator.push(
                context,
                MaterialPageRoute(
                  builder: (context) => GateResultPage(
                    chapter: widget.chapter + 1,
                    username: widget.username,
                  ),
                ),
              );
            },
            child: Text('‡πÑ‡∏õ‡∏ï‡πà‡∏≠'),
          ),
